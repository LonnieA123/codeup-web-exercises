function isOdd(number){
    if(number % 2 == 1){
        return ("this number is odd")
    } else {
        return("this number is even")
    }
}

function isEven(number){
    if(number % 2 == 0){
        return ("this number is even")
    } else {
        return("this number is odd")
    }
}

